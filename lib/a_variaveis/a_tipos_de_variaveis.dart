void main() {
  // Tipos pre definidos
  // String, int, double, bool e num

  //Declaracoes
  String curso = 'Analise e desenvolvimento de sistemas';
  int acessoEmAnos = 2;
  double valor = 1196.0;
  bool turmaAberta = false;

  //Tipos nulos
  //Todos os tipos podem 2 valores(Nonull e Null)
  String? cursoNulo;
  int? acessoEmAnosN;
  double? valorN;
  bool? turmaAbertaN;

  print([
    curso,
    acessoEmAnos,
    valor,
    turmaAberta,
    cursoNulo,
    acessoEmAnosN,
    valorN,
    turmaAbertaN
  ]);
}
